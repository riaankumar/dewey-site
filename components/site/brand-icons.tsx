import type { CSSProperties, SVGProps } from "react";

/**
 * Integration brand marks, in each brand's own colour.
 *
 * Path data is from Simple Icons (https://simpleicons.org), which is CC0. Every
 * brand name and logo remains a trademark of its owner and appears here only to
 * identify the products Emulate integrates with.
 *
 * Two colours per brand, because a single one cannot work on both themes: some
 * marks are near-black (Notion #000, Zendesk, Slack) and vanish on a dark card,
 * and Intercom's mint (#6AFDEF) vanishes on white. `hex` renders in light mode,
 * `darkHex` in dark. Where they differ, darkHex is the brand's own light-on-dark
 * treatment — except Intercom, whose light value is its mint deepened to stay
 * legible while keeping the hue.
 *
 * Not included: Salesforce, Slack and Microsoft 365 were pulled from Simple
 * Icons upstream, and ServiceNow/Workday were never in it — see the note in
 * demo-section.tsx. Their marks here come from an older Simple Icons release.
 */
type IconProps = SVGProps<SVGSVGElement>;

/**
 * Renders at `hex`, swapping to `darkHex` under `.dark`. Colour rides on two
 * CSS vars so a single element covers both themes; `fill` follows currentColor.
 */
function icon(path: string, title: string, hex: string, darkHex: string) {
  const Icon = ({ style, className, ...props }: IconProps) => (
    <svg
      viewBox="0 0 24 24"
      fill="currentColor"
      role="img"
      aria-label={title}
      className={["text-[var(--brand-logo)]", "dark:text-[var(--brand-logo-dark)]", className]
        .filter(Boolean)
        .join(" ")}
      style={{ "--brand-logo": hex, "--brand-logo-dark": darkHex, ...style } as CSSProperties}
      {...props}
    >
      <path d={path} />
    </svg>
  );
  Icon.displayName = title.replace(/\s/g, "") + "Icon";
  return Icon;
}

