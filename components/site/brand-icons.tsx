/**
 * Text fallback for brands with no freely licensed mark. Using the plain name is
 * nominative use and needs no permission, unlike reproducing the logo.
 */
export function BrandWordmark({
  name,
  className,
}: {
  name: string;
  className?: string;
}) {
  return (
    <span
      className={className}
      style={{ fontSize: "0.62rem", fontWeight: 700, letterSpacing: "-0.01em" }}
    >
      {name}
    </span>
  );
}
